let handler = async (m, {
    command,
    usedPrefix,
    args
}) => {
    let imgr = flaaa.getRandom()
    let user = global.db.data.users[m.sender]
    const listItems = Object.fromEntries(Object.entries(items[command.toLowerCase()]).filter(([v]) => v && v in user))

    let text = ''
    let footer = ''
    let image = ''
    let buttons = ''
    text = (command.toLowerCase() == 'buy' ?
        (`
*${htki} BUY ${htka}*
`.trim()) :
        (`
*${htki} SELL ${htka}*
`.trim())
    )
    footer = (command.toLowerCase() == 'buy' ?
        (`
💁🏻‍♂ ᴛɪᴩ :
⮕ ᴛᴏ ʙᴜʏ ɪᴛᴇᴍs:
${usedPrefix}${command} *[item] [quantity]*
★ ᴇxᴀᴍᴩʟᴇ:
${usedPrefix}${command} *potion 10*

–––––––––––––––––––––––––
🔖 ɪᴛᴇᴍs ʟɪsᴛ :
${Object.keys(listItems).map((v) => {
        let paymentMethod = Object.keys(listItems[v]).find(v => v in user)
        return `👉 1 ${global.rpg.emoticon(v)}${v} ﹫ ${listItems[v][paymentMethod]} ${global.rpg.emoticon(paymentMethod)}${paymentMethod}`.trim()
    }).join('\n')}
`.trim()) :
        (`
💁🏻‍♂ ᴛɪᴩ :
⮕ ᴛᴏ sᴇʟʟ ɪᴛᴇᴍs:
${usedPrefix}${command} *[item] [quantity]*
★ ᴇxᴀᴍᴩʟᴇ:
${usedPrefix}${command} *potion 10*

–––––––––––––––––––––––––
🔖 ɪᴛᴇᴍs ʟɪsᴛ :
${Object.keys(listItems).map((v) => {
        let paymentMethod = Object.keys(listItems[v]).find(v => v in user)
        return `👉 1 ${global.rpg.emoticon(v)}${v} ﹫ ${listItems[v][paymentMethod]} ${global.rpg.emoticon(paymentMethod)}${paymentMethod}`.trim()
    }).join('\n')}
`.trim())
    )
    image = (command.toLowerCase() == 'buy' ?
        (imgr + 'buy') :
        (imgr + 'sell')
    )
    buttons = (command.toLowerCase() == 'buy' ?
        ([
            [`ʙᴜʏ ʟɪᴍɪᴛ`, `${usedPrefix}buy limit`],
            [`ʙᴜʏ ᴩᴏᴛɪᴏɴ`, `${usedPrefix}buy potion`]
        ]) :
        ([
            [`sᴇʟʟ ᴩᴏᴛɪᴏɴ`, `${usedPrefix}sell potion`],
            [`sᴇʟʟ ᴛʀᴀsʜ`, `${usedPrefix}sell trash`]
        ])
    )
    const item = (args[0] || '').toLowerCase()
    const total = Math.floor(isNumber(args[1]) ? Math.min(Math.max(parseInt(args[1]), 1), Number.MAX_SAFE_INTEGER) : 1) * 1
    if (!listItems[item]) return conn.sendFile(m.chat, image, '', text + '\n' + footer, m)
    if (command.toLowerCase() == 'buy') {
        let paymentMethod = Object.keys(listItems[item]).find(v => v in user)
        if (user[paymentMethod] < listItems[item][paymentMethod] * total) return conn.sendFile(m.chat, imgr + 'lowcredit',
            '',
            `*–『 INSUFFICIENT CREDIT 』–*\nʏᴏᴜ ɴᴇᴇᴅ ᴇxᴛʀᴀ *${(listItems[item][paymentMethod] * total) - user[paymentMethod]}* ${global.rpg.emoticon(paymentMethod)}${paymentMethod} ᴛᴏ ʙᴜʏ *${total}* ${global.rpg.emoticon(item)}${item}.
ʏᴏᴜ'ᴠᴇ *${user[paymentMethod]}* ${global.rpg.emoticon(paymentMethod)}${paymentMethod} ɪɴ ʙᴀɢ.
–––––––––––––––––––––––––
💁🏻‍♂ ᴛɪᴩ :
ᴏᴩᴇɴ ᴄʀᴀᴛᴇs & ᴄᴏʟʟᴇᴄᴛ ʀᴇᴡᴀʀᴅs.
⮕ ᴛᴏ ᴏᴩᴇɴ ᴄʀᴀᴛᴇs:
.open crate
⮕ ᴛᴏ ᴄᴏʟʟᴇᴄᴛ ʀᴇᴡᴀʀᴅs:
.adventure | .daily | .monthly
`.trim(), m)
        user[paymentMethod] -= listItems[item][paymentMethod] * total
        user[item] += total
        return conn.sendFile(m.chat, imgr + 'bought', '',
            `*${htki} BOUGHT ${htka}*\nʏᴏᴜ *ʙᴏᴜɢʜᴛ ${total} ${global.rpg.emoticon(item)}${item}*.
`.trim(), m)
    } else {
        if (user[item] < total) return m.reply(`You don't have enough *${global.rpg.emoticon(item)}${item}* to sell, you only have ${user[item]} items`)
        user[item] -= total
        user.money += listItems[item].money * total
        return conn.sendFile(m.chat, imgr + 'sold', '',
            `*${htki} SOLD ${htka}*\nʏᴏᴜ *sᴏʟᴅ ${total} ${global.rpg.emoticon(item)}${item}*.
`.trim(), m)
    }
}

handler.help = ['buy', 'sell'].map(v => v + ' [item] [count]')
handler.tags = ['rpg']
handler.command = /^(buy|sell)$/i

handler.disabled = false

export default handler

function isNumber(number) {
    if (!number) return number
    number = parseInt(number)
    return typeof number == 'number' && !isNaN(number)
}